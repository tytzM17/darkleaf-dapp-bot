async function getTokenBalance() {
  if (!signer || !dlcAbi) {
    return
  }
  const erc20Contr = new ethersjs.Contract(
    ERC20_CONTRACT_ADDRESS,
    dlcAbi,
    signer,
  )
  const bal = await erc20Contr.balanceOf(userAccount)

  return ethersjs.utils.formatUnits(bal, 18)
}

getTokenBalance()
  .then((results) => {
    console.log(results)
    if (!results) {
      return
    }
    document.getElementsByClassName('tokens')[0].innerText = `${results} DLCs`
  })
  .catch((error) => console.log(`Error getting token balance: ${error}`))

async function approveTx(_amount) {
  const erc20Contr = new ethersjs.Contract(
    ERC20_CONTRACT_ADDRESS,
    dlcAbi,
    signer,
  )
  const newAmount = `${new ethersjs.utils.parseEther(_amount)}`
  return await erc20Contr.approve(CONTRACT_ADDRESS, newAmount)
}
